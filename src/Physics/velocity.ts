export class Velocity2D {
  x: number
  y: number

  constructor(
    x: number,
    y: number
  ) {
    this.x = x
    this.y = y
  }
}

export class Velocity3D {
  x: number
  y: number
  z: number

  constructor(
    x: number,
    y: number,
    z: number
  ) {
    this.x = x
    this.y = y
    this.z = z
  }
}

